    private void mni_startMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mni_startMouseClicked
        QuizzView quizz;
        try {
            quizz = new QuizzView();
            pnl_principal.removeAll();
            pnl_principal.add(quizz);
            quizz.setLocation(pnl_principal.getWidth()/2 - quizz.getWidth()/2,
                    pnl_principal.getHeight()/2 - quizz.getHeight()/2);
            
            pnl_principal.updateUI();
        } catch (SQLException ex) {
            Logger.getLogger(Principal_aluno.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_mni_startMouseClicked

